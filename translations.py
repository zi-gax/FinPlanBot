def get_text(key: str, lang: str = 'fa', **kwargs) -> str:
    """Get translated text based on language. Supports format strings."""
    text = TRANSLATIONS.get(lang, TRANSLATIONS['fa']).get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except:
            return text
    return text

